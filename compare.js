//Compare Operator
//1.Equal Operator
console.log(2 == "2"); //true
console.log(2 === "2"); //false

//2.Not Equal Operator
console.log(2 != 2); //false
console.log('hello' != 'Hllo'); //true

//3.Stric Equal Operator
console.log(2 === 2); //true
console.log(2 === '2'); //false

//4.Stric Not Equal Operator
console.log(2 !== '2'); //true
console.log(2 !== 2); //false

